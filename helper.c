#include "header.h"

tid	count_neu(neu *l)
{
	tid	c;

	c = 0;
	while (l)
	{
		c++;
		l = l->nx;
	}
	return (c);
}

void	*neu_cp(neu *d, neu *o)
{
	d->id = o->id;
	d->tp = o->tp;
	d->in = o->in ;
	d->out = o->out;
	d->tr = o->tr;
	d->op = o->op;
	d->iv = o->iv;
	d->nx = o->nx;
	return ;
}
