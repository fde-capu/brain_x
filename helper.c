#include "header.h"

t_id	net_neu_concat(neu *a, net b)
{
	t_id	alen;
	net		new_b;

	alen = 0;
	while (a->next)
	{
		new_b.id = a->id;
		new_b.innov = a->innov;
		new_b.next = &b;
		b = new_b;
		alen++;
		a = a->next;
	}
	return (alen);
}
